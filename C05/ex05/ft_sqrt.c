int	ft_sqrt(int nb)
{
	int	nbr;

	nbr = 1;
	while (nbr * nbr <= nb && nbr < 46341)
	{
		if (nbr * nbr == nb)
			return (nbr);
		nbr++;
	}
	return (0);
}
