char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	fin;

	i = 0;
	fin = 0;
	while (dest[fin] != '\0')
	{
		fin++;
	}
	while (src[i] != '\0')
	{
		dest[fin] = src[i];
		fin++;
		i++;
	}
	dest[fin] = '\0';
	return (dest);
}
